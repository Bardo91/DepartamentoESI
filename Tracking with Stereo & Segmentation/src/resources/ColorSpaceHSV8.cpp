int bin2dec(string bin) {
	const char *cstr = bin.c_str();
	int len, dec = 0, i, exp;

	len = strlen(cstr);
	exp = len - 1;

	for (i = 0; i < len; i++, exp--)
		dec += cstr[i] == '1' ? pow(2, exp) : 0;
	return dec;
}

